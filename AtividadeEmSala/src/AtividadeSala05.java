public class AtividadeSala05 {
    //Concatenação
    public static void main(String[] args){
        char letra = 'S';
        char inicial = 'V';
        String texto = "Code";
        //Concatenação
        String temp = "";
        temp += "" + inicial + letra;
        System.out.println(temp);
        System.out.println(temp + texto);


    }
}

